def relation_to_luke(name):
    if name == "Darth Vader":
        return "Luke, I am your father."
    if name == "Leia":
        return "Luke, I am your sister."
    if name == "Han":
        return "Luke, I am your brother in law."
    if name == "R2D2":
        return "Luke, I am your droid."
    
print(relation_to_luke("Darth Vader"))